○ 데이터 베이스 사용자

    사용자 <-----> 데이터베이스관리시스템(DBMS) <-----> 데이터베이스

○ 사용자
- 데이터베이스 관리자(DBA) : 데이터베이스 설계와 정의, 관리 및 운영 등 
  데이터베이스 시스템을 관리하고 제어하는 사용자
- 응용프로그래머 : 데이터베이스를 실제적으로 설계하여 최종 사용자들의 요구에 맞는 
  인터페이스와 응용 프로그램을 개발
- 최종 사용자: 데이터베이스를 실질적으로 사용하는 사용자




