public class Ex_if_else {
	public static void main(String[] args) {
		int a = 9;
		if (a % 2 == 0) {
			System.out.println("짝수입니다."); //조건식이 true
		} else {
			System.out.println("홀수입니다."); //조건식이 false
		}//if-else
	}//main()
}//class

/*
 블럭 if문 : 조건을 판단하여 참일 때 혹은 거짓일 때 명령을 실행 
  
  if (조건식) {
  	true 실행문;
  } else {
  	false 실행문;
  }//if-else
  
조건식 안에는 반드시 비교연산자 혹은 논리연산자가 들어가야 한다.
*/
